package com.example.amiapp;

public class Ley {

    private String id;
    private Object idarticulo;
    private String estado;
    private String nombre;
    private String codDecreto;
    private String fechaPublicacion;
    private String descripcion;
    private String proponente;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Object getIdarticulo() {
        return idarticulo;
    }

    public void setIdarticulo(Object idarticulo) {
        this.idarticulo = idarticulo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCodDecreto() {
        return codDecreto;
    }

    public void setCodDecreto(String codDecreto) {
        this.codDecreto = codDecreto;
    }

    public String getFechaPublicacion() {
        return fechaPublicacion;
    }

    public void setFechaPublicacion(String fechaPublicacion) {
        this.fechaPublicacion = fechaPublicacion;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getProponente() {
        return proponente;
    }

    public void setProponente(String proponente) {
        this.proponente = proponente;
    }


}


